#include "../headers/minishell.h"

int ft_isspace(char c)
{
    return (c == ' ' || c == '\t');
}

int ft_special_char(char c)
{
    return (c == '>' || c == '<' || c == '|' || c == ' ' || c == '\t' || c == ':');
}